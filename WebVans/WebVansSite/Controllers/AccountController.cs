        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(FormCollection form, string returnUrl)
        {
            string email = Request.Form["Email"];
            string password = Request.Form["Senha"];
            bool rememberMe = Request.Form["LembrarDeMim"] == "1" ? true : false;

            if (string.IsNullOrEmpty(email) || string.IsNullOrWhiteSpace(password)) return View();

            var user = db.Users.FirstOrDefault(u => u.Email == email);

            if (user != null)
            {
                if (!user.UserProfileInfo.UsuarioValidado)
                {
                    return RedirectToAction("Index", "AguardeValidacao");
                }
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(email, password, rememberMe, shouldLockout: false);

            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = rememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "E-mail e/ou Senha inválidos.");
                    return View();
            }
        }

